        <p  class="text-justify"> O tecido social, asociativo e empresarial do software libre galego queremos amosar a nosa preocupación ante a decisión do Goberno galego de implantar software privativo no ensino público e o dispendio económico de 13,5 millóns de euros en licenzas de código pechado. Malia o publicado na prensa sobre a gratuidade da implantación de software ofimático, ambos acordos recentes, están claramente vinculados desde o punto de vista da planificación. Isto traerá consecuencias, dificultando a interoperabilidade dos sistemas e obstaculizando o uso de estándares abertos na Administración pública.
		</p>
		
		 <p  class="text-justify"> O convenio asinado entre a Conselleira de Educación, a directora da AMTEGA (Axencia para a Modernización Tecnolóxica de Galicia) e a presidenta de Microsoft España afecta ao tecido produtivo das TIC: incrementa a dependencia tecnolóxica dos futuros profesionais e mingua a súa capacidade de investigación, autoaprendizaxe e adaptación ás ferramentas do mercado, capacidade básica agardada en perfís profesionais tecnolóxicos. Como primeiro aspecto criticable, temos que dicir que o texto do convenio non é público, polo que non podemos deixar de lado preocupacións sobre o seu alcance, garantías e conveniencia. O alumnado de formación profesional informática será instruído nas plataformas privativas de Microsoft®. Isto provocará un efecto contaxio nas nosas empresas TIC converténdoas en consumidoras de tecnoloxía no canto de ser produtoras, por non falar da súa extensión ao ámbito directo das familias.
		</p>
		
		 <p  class="text-justify"> As empresas de software libre en Galicia son referentes no contexto TIC español. Isto débese en boa medida á capacidade de contar con profesionais cunha formación ampla e coas vantaxes que ofrece o código aberto. Esta tecnoloxía socialmente aberta permite que o alumnado poida investigar en profundidade as ferramentas informáticas innovando novos usos e funcionalidades. Ademais permítelle aos docentes propor metodoloxías innovadoras na aula, como a aprendizaxe baseada en retos ou proxectos. Esta forma de traballar logra recrear a forma de traballo das empresas, mentres que coas solucións privativas non é posíbel adestrarse en desenvolvemento.
		</p>
		
		 <p  class="text-justify"> O proceso de formación debe garantir que o alumnado acade pericias que posteriormente lle permitan adaptarse de xeito sinxelo á evolución das ferramentas do mercado, fomentar a súa capacidade de autoaprendizaxe para a vida real, polo que o argumento de incorporar no proceso formativo as versións máis novidosas do software privativo, é unha vía morta, antes ben, mingua a capacitación do alumnado nas destrezas altamente valoradas no tecido empresarial galego en xeral, e no sector TIC en particular.
		</p>
		
		 <p  class="text-justify"> Asemade da preocupación polo almacenamento ou explotación dos datos de perfís persoais dos nosos menores que poida chegar a facer unha multinacional estranxeira, os investimentos en ferramentas de software privativo representan unha lápida para o futuro do alumnado, xa que os desinvestimentos en servizos de software libre representan unha degradación da creación de emprego nas empresas galegas que no futuro poderán empregar ao actual alumnado. Polo contrario, os investimentos en software privativo deixan ás empresas autóctonas como revendedoras de produtos cunhas ínfimas marxes de negocio, repercutindo a maior parte do investimento nas multinacionais estranxeiras e non xerando riqueza local. O investimento en software privativo para a ensinanza aboca o alumnado de vocación tecnolóxica a unha migración forzada, e polo tanto, un apoio ao terríbel proceso de descapitalización que sofre Galicia, ademais de frear o desenvolvemento do tecido empresarial tecnolóxico local.
		</p>
		
		 <p  class="text-justify"> As asociacións Aberteo, AGASOL, Asociación Informática Amigus, Bricolabs, Comunidade o Zulo, Ghandalf, GPUL, Nova Escola Galega, Proxecto Trasno, Recuncho Maker e Xeopesca, instamos a Xunta de Galicia a desbotar o acordo asinado con Microsoft ao ser prexudicial para a sociedade galega. Ademais convidamos á Consellería de Educación e á AMTEGA a ter cantas reunións sexan necesarias para afortalar a implantación de tecnoloxías libres.
		</p>
		
		
